def lenstrings(s1, s2,):
    if len(s1) == len(s2):
        print('Both strings are equal')
    else:
        print('Both strings are not equal')
        
lenstrings('Pasta', 'Briyani')

lenstrings('Honda', 'Jeep')

lenstrings('Harshith', 'Lohith')

lenstrings('Midboe', 'Lohith')

lenstrings('Lohith', 'Saarth')

